public class UFO {
    public String date;
    public String time;
    public String shape;
    public int duration;
    public double latitude;
    public double longitude;

    public UFO(String dt, String tm, String shp, int dur, double lat, double lon) {
        this.date = dt;
        this.time = tm;
        this.shape = shp;
        this.duration = dur;
        this.latitude = lat;
        this.longitude = lon;
    }
}

